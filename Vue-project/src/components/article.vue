十九大的主题是：不忘初心，牢记使命，高举中国特色社会主义伟大旗帜，决胜全面建成小康社会，夺取新时代中国特色社会主义伟大胜利，为实现中华民族伟大复兴的中国梦不懈奋斗。<br>
抚今思昔，展望未来，每一位共产党人只有不忘初心，继续前进，我们党的事业才能善始善成，方得始终。<br>
“不忘初心，方得始终”解读自《严华经》，意思是一个人做事情，始终如一的保持当初的信念，最后就一定能得到成功。<br>
五年前，“我志愿成为中华人民共和国人民警察，献身于崇高的人民公安事业，坚决做到对党忠诚、服务人民、执法公正、纪律严明……”铮铮的入警誓言,犹在耳畔。刚跨出校门懵懵懂懂的普通院校毕业生，机缘巧合下穿上警服，成为一名光荣的人民警察，是惊喜、是茫然、是骄傲，是一切从零开始。我知道，从那一刻起，我必须脚踏实地，勤学苦练，严守纪律，砥砺前行，争做合格的人民警察。<br>
随着时间流逝，入警时的那份淳朴之心却从未改变。而今作为一名警保民警，日常工作琐碎繁杂，虽然没有轰轰烈烈的英雄事迹，也没有可歌可泣的动人故事，但始终保持着一颗热诚服务的心，全心全意为全局民警办实事、解难题。回顾四年，深知要做好财务工作务必具备三个“心”。<br>
做业务要细心。财务工作涉及的是现金收付、银行结算等活动，直接关系到民警、职工个人、单位乃至国家的经济利益，一旦出现差错，就可能造成不可挽回的损失。在处理审核每年数以万计的单据时，务必要细心，集中精力、有条不紊；面对不断更新的财务政策，务必要细心，认真研读，反复推究。<br>
待同事要热心。作为财务窗口，面向的是全局民警，热心的接待每一位来报销的民警职工，用所学的专业知识、即时的财务政策解答他们的疑问。<br> 
遇诱惑要有决心。资金保障是全局正常运行的基础，财务人员作为资金和账务的执行者和管理者，必须谨慎职业，具有辨别是非的能力和抵制不良诱惑的决心。日常工作中，要认真学习并严格执行国家有关党风廉政建设规定，从思想上筑牢反腐倡廉、拒腐防变的堤防，坚决抵制各种社会不良风气的侵蚀和诱惑，爱岗敬业，诚实守信，廉洁自律，客观公正，坚持准则，提高技能，参与管理和强化服务。<br>
“不忘初心，牢记使命，永远奋斗”，无论身处何时何地，何种岗位，立足自身，积极进取，以饱满的政治热情、高昂的精神状态、真诚的服务态度，履职尽责，努力做一名合格的党员、合格的人民警察。<br>
将来，不论我们走了多久、多远，身心有多疲累，我们的内心深处都要永远记住自己当初是为什么而出发，身上背负了什么样的责任，只有铭记这些，人生才不会偏离方向。
铮铮誓言在耳畔，不忘初心得始终！<br>
