				在语义学上，“初心”就是出发时的目标、誓言或承诺。恩格斯说：“一个知道自己的目的，也知道怎样达到这个目的的政党，一个真正想达到这个目的的并且具有达到这个目的所必不可缺的顽强精神的政党—这样的政党将是不可战胜的。”近代以来，中国人民长期奋斗的历史足以说明，中国共产党就是这样的党。<br>
十九大报告核心关键词，不忘初心，牢记使命。“不能忘记为什么出发”，联想到我们的从警初心，那也是一份沉甸甸的责任和使命。正如人民警察警歌中所唱的那样，“伟大的祖国富裕我使命，复兴的民族给予我力量，忠诚的道路浴血荣光，英雄的足迹越走越长，对党忠诚，服务人民，执法公正，纪律严明。”我们不忘初心，砥砺前行，践行十九大精神，诠释人民警察警魂，始终在路上。<br>
我身边的这群战友，他们正像习总书记说得那样，在践行十九大精神的路上，勇往之前。在打击犯罪、保护人民的关键时刻挺身而出、冲锋在前，在重大安保任务面前不怕疲劳、连续作战，长期默默无闻、甘当无名英雄，在平凡的工作岗位上像老黄牛一样辛勤耕耘、当好“螺丝钉”。<br>
熬过无数通宵，面对危险无所畏惧的钟晓，我们港区分局乍浦派出所分管办案副所长。作为抓捕行动的负责人，在每次抓捕前，他都要亲自研究方案，布置行动，绝不允许计划出现一丝纰漏，纵使通宵达旦，他的脸上也看不出一丝疲惫。<br>
每一次抓捕他都在现场，每一次执行任务都有他坐镇，兄弟们说，“有钟所在，踏实，安心！”5.8非法拘禁案、5.5砸车盗窃案，6.21抢劫案件等等，在他的带领下，快速反应，快破案，快追赃。他，睿智，勇敢，更为重要的是他对公安工作始终保持着忠诚和热爱。他将每一次抓捕任务看成新的挑战，敢于攻坚克难，敢于往前冲，他将对公安工作的热爱转化为无尽的勇气与动力。<br>
公安工作不仅要面对穷凶极恶的犯罪分子，还有很多亟需帮助的普通群众。因此警察不仅需要铁血与勇气，还要有为民服务的柔情。<br>
他在面对群众时总是那么用心、耐心、有心，露出八颗牙的笑容，总是能让群众感受到37°的温暖，他就是我们的社区民警孙灿灿，大家都亲切地喊他一声“灿灿”。作为社区民警，他对辖区几户几人，细到哪家有几个人、分别叫什么名字、从事什么职业、有什么爱好，都十分清楚。他说：“我就是一名普通的社区民警，让群众平安快乐，就是我的责任。”而群众则说：“以前不知道什么叫社区民警。他来了之后，我们才知道，原来社区民警就是政府派来给我们解决困难的。”金阿姨是灿灿在走访中认识的，早年丈夫去世，儿子又身患残疾，娘俩相依为命，生活着实清贫。灿灿把阿姨一家作为重点照顾对象，并叮嘱“阿姨，有事一定找我，我保证随叫随到。”金阿姨说“我是不幸的，但是我又是幸运的，因为我有一个人民警察儿子。”得知阿姨想为身患残疾的儿子办残疾证时，灿灿主动帮忙打听，带着母子二人跑医院、跑政府、跑残联。终于，把证给办了下来。当他把证送到老人手上时，老人感动的紧紧握着他的手不肯放开。<br>
正是有了这些不忘初心，始终砥砺前行的平凡战士，才有了我们社会的平安和安宁。<br>
我们郑重宣誓：我保证忠于中国共产党，忠于祖国，忠于人民，忠于法律；服务命令，听从指挥；严守纪律，保守秘密；秉公执法，清正廉洁；恪尽职守，不怕牺牲；全心全意为人民服务。我愿现身于崇高的人民公安事业，为实现自己的誓言而努力奋斗。<br>
