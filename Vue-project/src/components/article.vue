在繁华的都市，当我们登高远眺时，映入你眼帘的是数不清的高楼大厦，那你可曾想到支撑大厦的那些默默无闻的颗颗基石吗？它们永远没有大厦的雄伟壮丽，没有令人瞩目的绚丽光彩，更不会听到路人的赞叹，可它们依然坚守在自己平凡的岗位，相互团结，手挽着手，奉献出自己所拥有的一切，我喜欢这些可爱的基石，也愿做一颗社会的基石，筑起社会的平安，换来万家灯火的宁静，把我的青春和汗水奉献给我热爱的公安事业。<br>
习近平同志在党的十九大报告中把坚持以人民为中心作为新时代坚持和发展中国特色社会主义的重要内容。他强调：人民是历史的创造者，是决定党和国家前途命运的根本力量。必须坚持人民主体地位，坚持立党为公、执政为民，践行全心全意为民服务的根本宗旨，把党的群众路线贯彻到治国理政全部活动之中，把人民对美好生活的向往作为奋斗目标，依靠人民创造历史伟业。我是一名社区民警，作为一名人民警察，全心全意为民服务，保一方平安是职责所在，我要以十九大精神来引领新时代的公安工作。在这平凡的岗位上，踏踏实实做事，用实际行动践行一名普通警察为人民服务的誓言，让闪光的警徽渲染着一身绽蓝。<br>
因为社区民警服务对象是社区居民，要想服务好，就必须深入到他们中间，了解他们，熟悉社区环境。刚到社区时由于我是宁波人，不熟悉海盐方言，沟通存在障碍，为尽快适应工作，我努力学习海盐方言，在工作中，尽量让同事们讲海盐话，提高沟通能力，通过一段时间的努力，听海盐话的能力大大提高，有时也能用海盐话和社区居民进行交流，我觉得心和他们贴得更近了；同时我还找了一张辖区地图熟悉周边的道路、建筑和小区的名称，下班没事，就一个人走街串巷，不为在商场购物、寻找可口的美食，而是了解社区建筑物的位置和道路走向，用心记忆，为今后能及时处警办案奠定基础。同事们开玩笑说“社区民警就是运动员”，我也深有体会。社区走访是最考验跑功的，每天根据工作的安排，定期走访一些辖区的居民，及时了解他们的想法，熟悉他们居住的位置，做到心中有数。<br>
 在这几年的工作经历中，我感触最深的是作好社区民警要有“三心”，首先，要有爱心，每一个社区就是一个大家庭，每一个居民，都是我们的亲人，在工作中就像关心自己家人一样，关心他们，了解他们，认识他们，不但要了解每户居民的家庭成员，工作状况，还要了解他们的住址和生活、爱好，做到心中有数，及时掌握社区的人员变化，更重要的是通过访谈尽最大努力，解决社区居民的困难；其次，工作要细心，在社区走访、巡逻和处理纠纷、案件的过程中，不是走马观花，而是观察社区环境的变化，通过街头巷尾百姓们热论的话题，了解社区治安状况、治安安全隐患和社区人员关心的热点问题和时间，及时提醒广大居民增强安全防范的意识，减少社区案件的发生，增加社区的团结；第三，要有恒心，我从作社区民警的那一天就明白，社区民警的工作是一项烦杂、琐碎的工作，虽然细小，但是关系着千家万户的幸福和安宁，每天都是很多诸如邻里纠纷、家庭不和、门打不开、丢了东西等在别人看起来的很小的小事，我都会满怀工作热情认真处理，因为我知道，作为社区民警，没有小事，社区居民的事都是大事，耐心了解事情原委，一件一件加以落实解决，如果这些小事处理不好，就会影响家庭和睦、邻里团结、社区治安。<br>
每当我走在熟悉的社区，看着人们轻松的脚步，不时和迎面走过熟识的老人、可爱的孩子打着招呼，眼前闪过曾经调解过家庭不和，又甜蜜如初的夫妻背影，还有一张张幸福快乐的笑脸，我心中升起乐无限的自豪，早已忘记了雪夜巡逻的辛苦，假日值班的疲劳，社区中走访、调解中被误解的委屈，走痛的双脚，故乡亲人的思念，有的是对社区民警工作的热爱，有的是投入社区治安工作的热情和动力，我愿做一颗平安基石，为构建和谐美丽社会，贡献一点绵薄之力。<br>
