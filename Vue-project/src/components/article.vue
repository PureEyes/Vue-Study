社区民警许佳，警校毕业后被分配至派出所户籍窗口，为了让自己在最短的时间内熟悉相关法律法规，她放弃休息时间，认真阅读并做好笔记，厚厚的笔记本里记满了她的重点、难点、要点，在她的努力下，一个多月的时间，从简单的身份证办领、分户、户口迁移到疑难问题、特殊问题，都能熟练地操作。2016年8月，岗位的调动让她手足无措，为尽快投入到新的工作，熟悉自己的辖区，许佳把自己才两周岁的孩子交给婆婆，放弃中午和晚上的休息时间，一门心思地投入到了工作中去。手绘地图、拍摄照片、走访排摸、发放名片……只要她能想到的方法，她都用了上去<br>，上班时间走访企业，午休、晚上的时间走访群众，短短两个多星期，辖区的群众竟然都知道这次村里来了一个“美女警察”，“许佳”这个名字也渐渐地在辖区有个知名度……
